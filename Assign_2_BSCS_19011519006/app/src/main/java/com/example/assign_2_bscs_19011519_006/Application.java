package com.example.assign_2_bscs_19011519_006;

public class Application {
    private String StudentName, FatherName, RegistrationNo;

    public String getStudentName() {
        return StudentName;
    }

    public String setStudentName(String studentName) {
        StudentName = studentName;
        return StudentName;
    }

    public String getFatherName() {
        return FatherName;
    }

    public String setFatherName(String fatherName) {
        FatherName = fatherName;
        return FatherName;
    }

    public String getRegistrationNo() {
        return RegistrationNo;
    }

    public String setRegistrationNo(String registrationNo) {
        RegistrationNo = registrationNo;
        return RegistrationNo;
    }

}
